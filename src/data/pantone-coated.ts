/** Search Pantone catalog by code (partial match) */
export function searchPantone(query: string): PantoneColor[] {
  const q = query.toLowerCase().trim();
  if (!q) return PANTONE_CATALOG.slice(0, 20);
  return PANTONE_CATALOG.filter(p => p.code.toLowerCase().includes(q)).slice(0, 30);
}
